    private void tbVendaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbVendaMouseClicked
        int row = tbVenda.getSelectedRow();
        int id = 0;
        if (row >= 0) {
            id = (int) tbVenda.getValueAt(row, 0);
        }

        try {
            javax.swing.table.DefaultTableModel modelTbl = (javax.swing.table.DefaultTableModel) tbItensVenda.getModel();
            modelTbl.setRowCount(0);
            java.util.List<ItensVenda> lista = controller2.listarItens(id);
            for (ItensVenda v : lista) {
                modelTbl.addRow(new Object[]{v.getIdProduto().getNomeProduto(), v.getQuantiProduto(), v.getPrecoUnit()});
            }
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao listar: " + ex.getMessage());
            ex.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Aqui" + ex.getMessage());
        }
    }//GEN-LAST:event_tbVendaMouseClicked

