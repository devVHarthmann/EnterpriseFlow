    private void initTableModelVenda() {
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                new String[]{"Id", "Data Venda", "Valor Total", "Vendedor", "CPF"}
        ) {
            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        tbVenda.setModel(model);
    }

    private void atualizarTabelaVenda() {
        try {
            javax.swing.table.DefaultTableModel modelTbl = (javax.swing.table.DefaultTableModel) tbVenda.getModel();
            modelTbl.setRowCount(0);
            java.util.List<Venda> lista = controller.listar();
            for (Venda v : lista) {
                modelTbl.addRow(new Object[]{v.getIdVenda(), v.getDataVenda(), v.getValorTotal(), v.getIdVendedor().getNome(), v.getIdCliente().getNome()});
            }
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao listar: " + ex.getMessage());
            ex.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Aqui" + ex.getMessage());
        }

    }

